-- Run xmonad with the settings you specify. No need to modify this.
--
main = do
  xmproc <- spawnPipe "xmobar -x 0 /home/sumit/.config/xmobar/xmobarrc"
  xmonad $ docks def {
      -- simple stuff
        terminal           = myTerminal,
        focusFollowsMouse  = myFocusFollowsMouse,
        clickJustFocuses   = myClickJustFocuses,
        borderWidth        = myBorderWidth,
        modMask            = myModMask,
        workspaces         = myWorkspaces,
        normalBorderColor  = myNormalBorderColor,
        focusedBorderColor = myFocusedBorderColor,

      -- key bindings
        keys               = myKeys,
        mouseBindings      = myMouseBindings,

      -- hooks, layouts
        layoutHook         = myLayout,
        manageHook         = myManageHook,
        startupHook        = myStartupHook,
        handleEventHook    = myEventHook,
        logHook            = dynamicLogWithPP $ def {
            ppOutput = \x -> hPutStrLn xmproc x
            , ppCurrent = xmobarColor "#50fa7b" "" . wrap "[" "]"
            , ppVisible = xmobarColor "#ffffff" ""
            , ppHidden = xmobarColor "#8ae0fc" ""
            , ppHiddenNoWindows = xmobarColor "#c792ea" ""
            , ppTitle = xmobarColor "#b3afc2" "" . shorten 40
            , ppSep = "<fc=#666666> <fn=1>|</fn> </fc>"
            , ppUrgent = xmobarColor "#c4550" "" . wrap "!" "!"
            , ppOrder = \(ws:l:t:ex) -> [ws, l]++ex++[t]
            }
        } `additionalKeysP`
        [ ("M-w", spawn "librewolf")
